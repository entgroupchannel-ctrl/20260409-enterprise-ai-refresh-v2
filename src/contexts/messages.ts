/* ── Type-safe key generation ──
 * Generates dot-notation keys like "nav.products", "admin.dashboard"
 * for autocomplete + type checking when calling t("...")
 */
type DotPaths<T, P extends string = ""> = {
  [K in keyof T & string]: T[K] extends string
    ? `${P}${P extends "" ? "" : "."}${K}`
    : T[K] extends object
    ? DotPaths<T[K], `${P}${P extends "" ? "" : "."}${K}`>
    : never;
}[keyof T & string];

export type TranslationKey = DotPaths<typeof messages.th>;
